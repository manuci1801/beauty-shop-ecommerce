const mongoose = require("mongoose");

const contactSchema = mongoose.Schema({
  tag: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model("contacts", contactSchema);
