const mongoose = require("mongoose");

const categorySchema = mongoose.Schema({
  name: String,
  description: {
    type: String,
    default: "",
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model("categories", categorySchema);
